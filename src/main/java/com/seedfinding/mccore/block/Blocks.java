	public static Block register(MCVersion implementedVersion, Pair<MCVersion, Integer>[] rawIds, Pair<MCVersion, String>[] rawNames) {
		Set<MCVersion> mcVersions = new HashSet<>();
		HashMap<MCVersion, Integer> ids = new HashMap<>();
		for (Pair<MCVersion, Integer> rawId : rawIds) {
			mcVersions.add(rawId.getFirst());
			ids.put(rawId.getFirst(), rawId.getSecond());
		}
		HashMap<MCVersion, String> names = new HashMap<>();
		for (Pair<MCVersion, String> rawName : rawNames) {
			mcVersions.add(rawName.getFirst());
			names.put(rawName.getFirst(), rawName.getSecond());
		}
		List<MCVersion> sorted = mcVersions.stream().sorted(Comparator.reverseOrder()).collect(Collectors.toList());
		// you are guaranteed that implementedVersion exists in both rawIds and rawNames
		Integer latestId = ids.get(implementedVersion);
		String latestName = names.get(implementedVersion);
		Block block = null;
		for (MCVersion mcVersion : sorted) {
			latestId = ids.getOrDefault(mcVersion, latestId);
			latestName = names.getOrDefault(mcVersion, latestName);
			block = new Block(mcVersion, latestId, latestName, implementedVersion);
			VERSIONED_REGISTRY.computeIfAbsent(block.getVersion(), i -> new HashMap<>()).put(block.getId(), block);
			VERSIONED_ID_REGISTRY.computeIfAbsent(block.getVersion(), i -> new HashMap<>()).put(block.getIdentifier(), block);
		}
		assert block != null;
		LATEST_REGISTRY.put(block.getId(), block);
		LATEST_ID_REGISTRY.put(block.getIdentifier(), block);
		return block;
	}
